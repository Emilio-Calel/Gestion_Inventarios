    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = txtid.getText();
        String nombre = txtnombre.getText();
        String user = txtuser.getText();
        String contrasena = txtcontra.getText();
        //Lo de arriba se encarga de capturar la info de los cuadros de texto
        //Lo de abajo almacena la info en el archivo de texto
       try{
           FileWriter writer = new FileWriter("usuarios.txt",true);
           writer.write(""+id+","+nombre+","+user+","+contrasena);
           writer.write(System.getProperty("line.separator"));
           writer.close();
           JOptionPane.showMessageDialog(null, "Registro Existoso");
           
       }catch (IOException ex){
           Logger.getLogger(Pregistro.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

